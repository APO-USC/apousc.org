<h3>History</h3>
<em>This is an abridged version of the <a href="history_general.php">full chapter history</a>.</em>
<p>Alpha Kappa became the second chapter in Los Angeles when it was founded in 1934 at the University of Southern California.
The chapter's activation and first Initiation Ceremony were held on June 3 of that year.</p>
<p>The chapter was dormant during 1943-1948, due to World War II, and again from 1958-1967.
The Spring 1967 pledge class brought the chapter back to regular activity with a number of service projects.
The new group's first official recruitment in the fall of 1967 involved a radically different new-member program, which eliminated the pledge period.</p>
<p>Its experimentation over, the chapter adopted the traditional recruitment and pledge programs in the 1970s.
Although highly visible on campus during these years, the chapter's membership declined for Alpha Phi Omega until no member remained in 1976.
The chapter was officially declared inactive in January 1980.
In 1988 a student held meetings on campus to reactivate the chapter, which rechartered on October 28, 1989.</p>
<p>Over the years, membership again declined.
At the beginning of the 1992-1993 school year, a student came to find Alpha Kappa Chapter active, but with zero members.
He oversaw the chapter until a new pledge class could be initiated.
Due to a large portion of the active members graduating and internal problems, membership once again declined.</p>
<p>A successful Fall 1994 recruitment was able to double the membership.
In Fall 1996, a great recruitment brought in 28 new pledges, making Alpha Kappa Chapter 45 brothers strong.
However, once again plagued with internal struggles and graduating brothers, membership remained small with no recruitment period during the Fall 1997 semester.</p>
<p>The chapter experienced yet another small pledge class in Spring 2008.
But some internal changes, like that in the family structure, helped fuel competition, spirit, and thus interest in Alpha Phi Omega.
The chapter originally had three families: Alpha, Phi, and Omega, with Alpha the largest. Phi and Omega families were eventually combined into a new Kappa family.</p>
<p>An amazing Fall 2008 Rho class had over 40 pledges (more than 30 of whom crossed).
Following the upward trend, the Spring 2009 Sigma and Fall 2009 Tau pledge classes also boasted more than 40 pledges each.
Due to the growth of the chapter, the two-family system was reorganized back into the original three-family structure; Kappa was divided between the newly reinstated Phi and Omega families.</p>

